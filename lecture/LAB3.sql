SELECT E.LAST_NAME, E.HIRE_DATE
FROM EMPLOYEES E
WHERE E.HIRE_DATE > (SELECT HIRE_DATE
                        FROM EMPLOYEES
                        WHERE LAST_NAME = 'Davies'
                        )

SELECT E.LAST_NAME, E.HIRE_DATE
FROM EMPLOYEES E, EMPLOYEES D
WHERE D.LAST_NAME = 'Davies' AND e.hire_date > D.HIRE_DATE


